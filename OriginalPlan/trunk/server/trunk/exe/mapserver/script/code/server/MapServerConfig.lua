-------------------------------------------------------
-- @class function
-- @name InitServerConfig
-- @description 初始化服务器配置
-- @param CGameConfig|config 服务器配置
-- @return bool|是否成功
-- @usage
function InitServerConfig(config)

	config:setBlockSize(15);	-- 视野块大小
	config:setBroadcastRange(2);	-- 视野环大小

	return true;
end
